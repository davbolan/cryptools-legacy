<h1 class="h4">Cryptools</h1>
<p>
  Cryptools es un grupo de herramientas orientado a la ofuscación y encriptación 
  de texto.
</p>
<p>
  Podrás desde transformar un texto a base64 hasta usar tu propio diccionario 
  que reempleace las palabras o carácteres que tu quieras. 
</p>
<p>
  ¡No pierdas el diccionario si alguna vez quieres recuperar el texto 
  encriptado!
</p>
